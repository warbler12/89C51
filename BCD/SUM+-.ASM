ORG 0000H
SJMP START
ORG 0030H

START:

MOV A,30H
MOV R1,#31H     ;address
LOOP:
CJNE @R1,#10000000B,JUG
INC 42H          ;10000000B=-128
INC R1
DEC A
JNZ LOOP
SJMP $

JUG:
JNC LOOP_
JC LOOP0_a
RET

LOOP0_a:
CJNE @R1,#00000000B,LOOP_a
INC 43H
INC R1
DEC A
JNZ LOOP
SJMP $
RET

LOOP_a:
INC 41H
INC R1
DEC A
JNZ LOOP
SJMP $
RET

LOOP_:
INC 42H
INC R1
DEC A
JNZ LOOP
SJMP $
RET
