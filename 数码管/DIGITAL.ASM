ORG 0000H
TABLE: DB 0A2H,062H,028H,074H,0A2H,028H,0A2H
SJMP START
ORG 0080H

START:

MOV DPTR, #TABLE

MOV R2,#00H        ;初始0位
MOV A,R2
MOVC A,@A+DPTR      ;第一个数
MOV P1,A            ;读数字
MOV A,#01111111B
MOV P2,A             ;选LED


LOOP1:
INC R2
CJNE R2,#07H,START
MOV A,R2
MOVC A,@A+DPTR
MOV P1,A
RLC A
MOV P2,A

MOV R3,#30H
MOV R4, #20
DJNZ R4, $
DJNZ R3, LOOP1
RET


SJMP $









